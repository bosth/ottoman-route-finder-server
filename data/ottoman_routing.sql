CREATE FUNCTION routing.create_shallow_curve(p1 public.geometry, p2 public.geometry) RETURNS public.geometry
    LANGUAGE plpgsql
    AS $$
DECLARE
    midpoint public.GEOMETRY;
    azimuth_angle FLOAT;
    normal_angle FLOAT;
    control_point public.GEOMETRY;
    offset_distance FLOAT;
    srid INTEGER;
BEGIN
    -- Ensure input points have the same SRID and are in a projected system for distance calculations
    srid := public.ST_SRID(p1);
    IF srid IS NULL OR srid != public.ST_SRID(p2) THEN
        RAISE EXCEPTION 'Both points must have the same projected SRID (e.g., 3857 or a local projection). Geographic SRIDs (e.g., 4326) will not work correctly with simple offsets.';
    END IF;

    -- 1. Find the midpoint of the straight line segment
    midpoint := public.ST_LineInterpolatePoint(public.ST_MakeLine(p1, p2), 0.5);

    -- 2. Calculate the azimuth (direction) from p1 to p2
    -- public.ST_Azimuth returns radians, but public.ST_Project works with radians too.
    azimuth_angle := public.ST_Azimuth(p1, p2);

    -- 3. Determine the normal (perpendicular) angle to the line
    -- Add or subtract PI/2 radians (90 degrees) to go left or right of the line
    -- Adding PI/2 bends the curve one way, subtracting the other way.
    normal_angle := azimuth_angle + PI()/2;

    offset_distance := public.ST_Distance(p1::public.geography, p2::public.geography) / 4;

    -- 4. Project the midpoint outwards along the normal by the offset distance
    -- public.ST_Project moves a point by distance (meters) and azimuth (radians)
    control_point := public.ST_Project(midpoint::public.geography, offset_distance, normal_angle)::public.geometry;

    -- 5. Create the final CIRCULARSTRING using the 3 points
    RETURN public.ST_CurveToLine(public.ST_SetSRID(
        public.ST_GeomFromText(
            'CIRCULARSTRING(' ||
            public.ST_X(p1) || ' ' || public.ST_Y(p1) || ', ' ||
            public.ST_X(control_point) || ' ' || public.ST_Y(control_point) || ', ' ||
            public.ST_X(p2) || ' ' || public.ST_Y(p2) || ')'
        ),
        srid
    ));
END;
$$;


