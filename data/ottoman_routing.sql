CREATE FUNCTION routing.find_route(start_id integer, end_id integer, available_modes text, year date) RETURNS SETOF routing.route_segment
    LANGUAGE plpgsql STRICT
    AS $$
    DECLARE
        r       RECORD;
        edge_source INT;
        edge_target INT;
        segment route_segment;
    BEGIN
        FOR r in EXECUTE 'SELECT node AS vertex_id,
                                edge AS edge_id,
                                cost AS cost
                        FROM pgr_dijkstra(
                            ''SELECT subquery.id, subquery.source, subquery.target, subquery.cost
                            FROM (
                                SELECT
                                e.id AS id,
                                e.source AS source,
                                e.target AS target,
                                e.cost AS cost
                                FROM edge e
                                WHERE 
                                  position(e.mode in ''''' || available_modes || ''''') > 0 AND (e.open is NULL OR
                                  ''''' || year || ''''' >= e.open) AND (e.close is NULL or 
                                  ''''' || year || ''''' <= e.close)
                            ) AS subquery
                            ORDER BY subquery.cost, subquery.source, subquery.target '', ' || start_id || ', ' || end_id || ', false)'
        LOOP
        IF (r.edge_id = -1) THEN
            RETURN;
        END IF;
        
        IF (r.cost < 0) THEN
            CONTINUE;
        END IF;

        SELECT edges.source INTO edge_source FROM edge AS edges WHERE r.edge_id = edges.id;
        SELECT edges.target INTO edge_target FROM edge AS edges WHERE r.edge_id = edges.id;
        SELECT edges.mode INTO segment.mode FROM edge AS edges WHERE r.edge_id = edges.id;
        segment.cost = r.cost;
        segment.id   = r.edge_id;

        IF (edge_source <> r.vertex_id) THEN
            SELECT ST_Reverse(edges.geom) INTO segment.geom FROM edge AS edges WHERE r.edge_id = edges.id;
            SELECT node.name INTO segment.source FROM node WHERE edge_target = node.id;
            SELECT node.name INTO segment.target FROM node WHERE edge_source = node.id;
        ELSE
            SELECT edges.geom INTO segment.geom FROM edge AS edges WHERE r.edge_id = edges.id;
            SELECT node.name INTO segment.source FROM node WHERE edge_source = node.id;
            SELECT node.name INTO segment.target FROM node WHERE edge_target = node.id;
        END IF;

        RETURN NEXT segment;
        END LOOP;
        RETURN;
    END;
    $$;


